public class for1 {
    public static void main(String[] args) {
        int start, end, step, sum;

        start = 7;
        end = 120;
        step = 3;
        sum = 0;

        for (int i = start; i < end; i = i + step){
            sum = sum + i;
            }
        System.out.println(sum);
        }
    }

